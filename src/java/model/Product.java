package model;

/**
 *
 * @author skyfc
 */
public class Product {
    int id;
    String name;
    String images;
    double price;
    String title;
    String description;

    public Product() {
    }

    public Product(int id, String name, String images, double price, String title, String description) {
        this.id = id;
        this.name = name;
        this.images = images;
        this.price = price;
        this.title = title;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImages() {
        return images;
    }

    public void setImages(String images) {
        this.images = images;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    
    
}
