package model;


/**
 *
 * @author skyfc
 */
public class Reservation {

    int id;
    String name;
    int numberofpeople;
    String date;
    int uid;

    public Reservation() {
    }

    public Reservation(int id, String name, int numberofpeople, String date, int uid) {
        this.id = id;
        this.name = name;
        this.numberofpeople = numberofpeople;
        this.date = date;
        this.uid = uid;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumberofpeople() {
        return numberofpeople;
    }

    public void setNumberofpeople(int numberofpeople) {
        this.numberofpeople = numberofpeople;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    
}
