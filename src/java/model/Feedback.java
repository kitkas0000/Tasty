package model;

/**
 *
 * @author skyfc
 */
public class Feedback {

    int id;
    String name;
    String feedback;
    int star;
    String date;
    int uid;

    public Feedback() {
    }

    public Feedback(int id, String name, String feedback, int star, String date, int uid) {
        this.id = id;
        this.name = name;
        this.feedback = feedback;
        this.star = star;
        this.date = date;
        this.uid = uid;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFeedback() {
        return feedback;
    }

    public void setFeedback(String feedback) {
        this.feedback = feedback;
    }

    public int getStar() {
        return star;
    }

    public void setStar(int star) {
        this.star = star;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

}
