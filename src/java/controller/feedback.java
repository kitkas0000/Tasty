    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        Account account = (Account) session.getAttribute("acc");

        request.getRequestDispatcher("feedback.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            String name = request.getParameter("name");
            String feedback = request.getParameter("feedback");
            String star = request.getParameter("star");
            HttpSession session = request.getSession();
            Account account = (Account) session.getAttribute("acc");
            int uid = account.getUid();

            if (feedback.isEmpty() || star.isEmpty()) {

                request.setAttribute("mess", "You need to fill in all information!");

                request.getRequestDispatcher("feedback.jsp").forward(request, response);

            } else if (Integer.parseInt(star) > 5 || Integer.parseInt(star) < 1) {
                request.setAttribute("mess", "You must enter number from 1 to 5!");

                request.getRequestDispatcher("feedback.jsp").forward(request, response);
            } else {

                DBContext db = new DBContext();

                db.addFeedback(name, feedback, star, uid);

                request.setAttribute("mess", "Thank for your feedback!");

                request.getRequestDispatcher("feedback.jsp").forward(request, response);

            }
        } catch (SQLException ex) {
            Logger.getLogger(reservation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

